const int menu_len = 2;
const char *menu[2];
int curr_selection;

static void draw_ui(float delta)
{
    ui_draw_border();
    ui_draw_debug(delta);
}

void st_menu_enter(game_t *game)
{
    clear();

    menu[0] = "  PLAY  ";
    menu[1] = "  QUIT  ";

    curr_selection = 0;
}

